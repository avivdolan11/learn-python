browser = "Google Chrome"

print(browser.find("C"))
print(browser.find("o"))
print(browser.find("oo"))
print(browser.find("G"))
print(browser.find("X"))

print(browser.find("o", 3))
print(browser.find("o", 2))

print(browser.index("C"))
print(browser.index("Z"))

# find finds the first index where substring occurs, rfind finds last index where substring occurs
