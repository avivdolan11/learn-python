mad_libs = "{} laughed at the {} {}."

print(mad_libs.format("Aviv", "green", "alien"))


mad_libs = "{0} laughed at the {1} {2}."

print(mad_libs.format("Aviv", "green", "alien"))


mad_libs = "{name} laughed at the {adjective} {noun}."

print(mad_libs.format(name = "Aviv", adjective =  "green", noun = "alien"))